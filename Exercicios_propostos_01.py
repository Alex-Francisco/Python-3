## Exercício 01 do livro Python 3 - Conceitos e Aplicações - Uma Abordagem Didática
""" Faça X = 0.0 e Y = 18. Verifique o tipo de dado que o Python atribuiu a cada um. Faça Z = X + Y e verifique
o resultado calculado e armazenado em Z. Verifique com qual tipo de dado foi criado o objeto Z. """

X = 0.0
Y = 18

print("O tipo atribuído para X foi: {}".format(type(X)))
print("O tipo atribuído para Y foi: {}".format(type(Y)))

Z = X + Y
print(" ")
print("O valor armazenado em Z é: {}".format(Z))
print("O tipo atribuído para Z foi: {}".format(type(Z))) 
