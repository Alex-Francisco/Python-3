## Exercício 03 do livro Python 3 - Conceitos e Aplicações - Uma Abordagem Didática
""" No IDLE, faça A = 'Questão 3', B = 25 e C = 3.9. Utilize o comando type para verificar qual
é o tipo de dado dos objetos A, B e C."""

A = "Questão 3"
B = 25
C = 3.9

print("O objeto 'A' contem o valor: {0}. Seu tipo é: {1}".format(A, type(A)))
print(" ")
print("O objeto 'B' contem o valor: {0}. Seu tipo é: {1}".format(B, type(B)))
print(" ")
print("O objeto 'C' contem o valor: {0}. Seu tipo é: {1}".format(C, type(C)))
