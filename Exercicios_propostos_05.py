""" Escreva a sequência de comandos necessária para o cálculo da área de um triângulo de
base 9 e altura 6."""

Base = 9
Altura = 6
Area = Base * Altura

print("Sendo a base = {0} e altura = {1}, a area resulta em {2}".format(Base, Altura, Area))
