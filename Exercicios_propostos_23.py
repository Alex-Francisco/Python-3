## Exercício 23 do livro Python 3 - Conceitos e Aplicações - Uma Abordagem Didática
""" Escreva um programa que mostre na tela todos os números de 1 a 10. Para fazer esse programa, será necessário usar o comando de repetição
while."""

cont = 1

while cont < 11:
    print(cont, end=" ")
    cont += 1
